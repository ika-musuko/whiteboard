class Info {
    private int x;
    private int y;

    public Info(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public int setX(int x) {
        this.x = x;
    }

    public int setY(int y) {
        this.y = y;
    }
}
